<h2>Objeto y duración</h2>
<ol class="clauses">
    <li>El afiliado se asocia a Serviconli para la gestión administrativa y operativa de su vinculación y aportes al sistema de seguridad social.</li>
    <li>La asociación tendrá duración indefinida mientras subsistan las obligaciones legales y contractuales.</li>
    <li>La cuota o contraprestación, cuando aplique, se regirá por el tarifario vigente y será informada por medios idóneos.</li>
    <li>Las partes resolverán controversias preferentemente por mecanismos conciliatorios antes de acudir a la jurisdicción ordinaria.</li>
    <li>El afiliado declara que la información suministrada es veraz y se compromete a actualizarla.</li>
    <li>Serviconli podrá dar por terminada la relación por causas legales o reglamentarias, con debido proceso.</li>
    <li>La protección de datos personales se regirá por la política de tratamiento adoptada por Serviconli.</li>
    <li>Las cesiones y endosos requerirán acuerdo escrito cuando la norma lo exija.</li>
    <li>El presente contrato reempluye manifestaciones previas contradictorias entre las partes sobre el mismo objeto.</li>
</ol>
